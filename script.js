document.addEventListener('DOMContentLoaded', function () {
    const searchLogo = document.querySelector('.nav_search_logo');
    const searchBarContainer = document.getElementById('search-bar-container');
    const searchBar = document.getElementById('search-bar');

    searchLogo.addEventListener('click', function (e) {
        e.stopPropagation();
        searchBarContainer.style.display = 
            searchBarContainer.style.display === 'block' ? 'none' : 'block';
        if (searchBarContainer.style.display === 'block') {
            searchBar.focus();
        }
    });

    document.addEventListener('click', function () {
        searchBarContainer.style.display = 'none';
    });

    searchBarContainer.addEventListener('click', function (e) {
        e.stopPropagation();
    });
});
